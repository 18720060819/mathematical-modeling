function [r]= calculate_r(X, Y)
% % 输入值：
    % X: 列向量
    % Y: 列向量，且与X同维度
% % 返回值：
    % r: X和Y的斯皮尔曼相关系数（第一种定义方法）
    
    RX = rank_data(X);  % 调用自定义函数 rank_data 来计算X的等级
    RY = rank_data(Y);  % 调用自定义函数 rank_data 来计算Y的等级
    d = RX - RY; % 计算X和Y等级差
    n = size(X,1); % 计算样本个数n
    r = 1 - (6 * sum(d .* d)) / (n * (n^2-1));  % 利用公式计算斯皮尔曼相关系数

end


