%产生一组[0，1]分布的随机数，采用余数法
%从文献中得知下列参数组较为适用：
%y的初值为1，w=7,模M=10^10(伪随机数周期为5*10^7)
%y的初值为1，w=5^13,模M=2^36(伪随机数周期为2^34,约2*10^10)
%y的初值为1，w=5^17,模M=2^42(伪随机数周期为2^40,约10^12)
%--------------------------------------------------------
%function x=uniform(y,w,M,n)
%n为要产生的随机数个数
function x=uniform(y,w,M,n)
for i=1:n
    x(i)=y./M;
    y=rem(w*y,M);%rem()为求余函数
end
